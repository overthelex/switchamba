ALL_BIGRAMS = {
    "en": BIGRAMS_EN,
    "ru": BIGRAMS_RU,
    "ua": BIGRAMS_UA,
}

# Penalty for unknown bigrams (log of a very small probability)
UNKNOWN_BIGRAM_SCORE = -1.0


def score_bigrams(text: str, language: str) -> float:
    """Score a text string against bigram frequencies for a language.

    Returns a score (higher = better match). The score is the average
    log-frequency of all bigrams in the text.
    """
    text = text.lower()
    bigrams_table = ALL_BIGRAMS.get(language, {})

    if len(text) < 2:
        return 0.0

    total_score = 0.0
    count = 0

    for i in range(len(text) - 1):
        bigram = text[i:i + 2]
        # Skip bigrams with spaces or non-alpha
        if not bigram[0].isalpha() or not bigram[1].isalpha():
            continue

        freq = bigrams_table.get(bigram)
        if freq is not None:
            total_score += math.log1p(freq)
        else:
            total_score += UNKNOWN_BIGRAM_SCORE
        count += 1

    if count == 0:
        return 0.0

    return total_score / count


def score_all_languages(texts: dict[str, str]) -> dict[str, float]:
    """Score texts (one per layout) against all language bigram tables.

    Args:
        texts: {"en": "hello", "ru": "руддщ", "ua": "руддщ"}

    Returns:
        {"en": 1.23, "ru": -0.5, "ua": -0.4}
    """
    scores = {}
    for lang in ("en", "ru", "ua"):
        text = texts.get(lang, "")
        if text:
            scores[lang] = score_bigrams(text, lang)
        else:
            scores[lang] = UNKNOWN_BIGRAM_SCORE
    return scores
